    <h1>Barangay Safety Regulations</h1>
    <p style="text-indent: 50px; text-align: justify;">
      Welcome to the official safety guidelines for our beloved Barangay
      PUP! The safety and well-being of every resident,
      visitor, and business within our community are our top priorities.
      These regulations are designed to foster a secure and harmonious
      environment for everyone. We urge all members of our community to
      familiarize themselves with these guidelines and to actively
      participate in their implementation.
    </p>

    <h2>General Safety Principles:</h2>
    <ol>
      <li>
        <b>Be Aware of Your Surroundings:</b> Always be vigilant and mindful
        of what is happening around you, especially when in public spaces or
        at night.
      </li>
      <li>
        <b>Report Suspicious Activities:</b> If you see anything unusual,
        suspicious, or potentially dangerous, do not hesitate to report it
        immediately to the Barangay Officials or the local police.
      </li>
      <li>
        <b>Cooperate with Authorities:</b> Fully cooperate with Barangay
        Officials, Tanods, and law enforcement officers in the performance
        of their duties to maintain peace and order.
      </li>
      <li>
        <b>Know Your Neighbors:</b> Familiarize yourself with your
        neighbors. A strong community bond can be a powerful deterrent to
        crime and a source of mutual support during emergencies.
      </li>
      <li>
        <b>Secure Your Property:</b> Always ensure your homes and businesses
        are properly secured with locks, gates, and other necessary security
        measures.
      </li>
    </ol>

    <h2>Specific Regulations and Guidelines:</h2>
    <h3>I. Peace and Order:</h3>
    <ol>
      <li>
        <strong>Curfew for Minors:</strong> <br>
        • A curfew is enforced for minors (under 18 years old) from (Start
        Time, e.g., 10:00 PM] to (End Time, e.g., 4:00 AM) unless
        accompanied by a parent or legal guardian, or engaged in an
        authorized activity.<br />
        • Parents/Guardians are responsible for ensuring their children
        adhere to the curfew.
      </li>
      <li>
        <strong>Noise Regulations:</strong><br>
        • Loud noises (e.g., karaoke, loud music, excessive shouting) are
        prohibited, especially during late hours ([Start Time, e.g., 10:00
        PM) to avoid disturbing neighbors. <br>
        • Special permits for events requiring amplified sound may be obtained from the Barangay Council.
      </li>
      <li>
        <strong>Vagrancy and Loitering:</strong>
        Loitering in public places without a legitimate purpose, especially
        at night, is discouraged and may be addressed by Barangay Tanods.
      </li>
      <li>
        <strong>Illegal Activities:</strong>
        Any form of illegal activity, including but not limited to illegal
        gambling, drug use/dealing, theft, and vandalism, is strictly
        prohibited and will be reported to the proper authorities.
      </li>
      <li>
        <strong>Carrying of Deadly Weapons:</strong>
        The unauthorized carrying of bladed weapons, firearms, or any other
        deadly weapon in public places is strictly prohibited.
      </li>
    </ol>

    <h3>II. Public Health and Sanitation:</h3>
    <ol>
      <li>
        <strong>Waste Management:</strong>
        <ul>
          <li>
            Segregate your waste properly (biodegradable, non-biodegradable,
            recyclables).
          </li>
          <li>
            Dispose of trash only at designated areas and during specified
            collection times.
          </li>
          <li>Burning of garbage is strictly prohibited.</li>
        </ul>
      </li>
      <li>
        <strong>Cleanliness of Surroundings:</strong> Maintain cleanliness
        in and around your property. Do not litter in public areas, canals,
        or roads.
      </li>
      <li>
        <strong>Dengue Prevention:</strong>
        <ul>
          <li>
            Actively participate in "4 o'clock habit" (search and destroy
            mosquito breeding sites).
          </li>
          <li>Ensure no stagnant water collects in your surroundings.</li>
        </ul>
      </li>
      <li>
        <strong>Responsible Pet Ownership:</strong>
        <ul>
          <li>
            Keep pets within your property and prevent them from roaming
            freely, especially if they are aggressive.
          </li>
          <li>Clean up after your pets in public spaces.</li>
          <li>Ensure pets are vaccinated against rabies.</li>
        </ul>
      </li>
    </ol>

    <h3>III. Traffic and Road Safety:</h3>
    <ol>
      <li>
        <strong>Speed Limit:</strong> Observe the designated speed limits
        within Barangay roads, especially in residential areas.
      </li>
      <li>
        <strong>Parking:</strong> Park vehicles responsibly and ensure they
        do not obstruct traffic flow, pedestrian pathways, or private
        driveways.
      </li>
      <li>
        <strong>Pedestrian Safety:</strong> Pedestrians are encouraged to
        use sidewalks and designated pedestrian lanes.
      </li>
      <li>
        <strong>Tricycle/Motorcycle Safety:</strong>
        <ul>
          <li>Drivers must possess valid licenses and registrations.</li>
          <li>Passengers and drivers of motorcycles must wear helmets.</li>
          <li>Overloading of tricycles is prohibited.</li>
        </ul>
      </li>
    </ol>

    <h3>IV. Fire Safety:</h3>
    <ol>
      <li>
        <strong>Electrical Safety:</strong> Ensure all electrical wiring and
        appliances are in good condition to prevent electrical fires. Avoid
        octopus connections.
      </li>
      <li>
        <strong>Flammable Materials:</strong> Store flammable materials
        safely and away from heat sources.
      </li>
      <li>
        <strong>Open Burning:</strong> Open burning of leaves, trash, or any
        materials is generally prohibited. Obtain necessary permits from the
        Barangay or Fire Department if absolutely necessary for specific
        purposes.
      </li>
      <li>
        <strong>Emergency Exits:</strong> Ensure homes and establishments
        have clear and accessible emergency exits.
      </li>
      <li>
        <strong>Know Your Fire Extinguisher:</strong> For businesses and
        public establishments, ensure fire extinguishers are available,
        functional, and employees know how to use them.
      </li>
    </ol>

    <h3>V. Emergency Preparedness:</h3>
    <ol>
      <li>
        <strong>Emergency Contact Numbers:</strong> Keep a list of important
        emergency contact numbers readily available (see below).
      </li>
      <li>
        <strong>Evacuation Plan:</strong> Familiarize yourself with the
        Barangay's evacuation plan and designated safe zones in case of
        natural disasters (e.g., floods, earthquakes).
      </li>
      <li>
        <strong>Family Emergency Kit:</strong> Prepare an emergency kit for
        your household with essential supplies (water, food, first-aid,
        flashlight, radio) in case of emergencies.
      </li>
      <li>
        <strong>Barangay Drills:</strong> Participate in Barangay-wide
        emergency drills and awareness campaigns.
      </li>
    </ol>

    <h3>Reporting and Enforcement:</h3>
    <ul>
      <li>
        Any violations of these regulations should be reported to the
        Barangay Officials or Barangay Tanods.
      </li>
      <li>
        Violators may be subject to appropriate penalties as stipulated by
        Barangay Ordinances or national laws.
      </li>
      <li>
        The Barangay Council reserves the right to amend or add to these
        regulations as necessary for the safety and welfare of the
        community.
      </li>
    </ul>
  </div>
